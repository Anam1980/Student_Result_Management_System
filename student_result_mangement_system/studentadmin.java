    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField2.getText();
        
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","anam198@mysql");
            
            Statement st = con.createStatement();
            ResultSet rs=st.executeQuery("select course, branch from student where rollno='"+rollno+"'");
            if(rs.next()){
                String course=(String)rs.getString("course");
                String branch=(String)rs.getString("branch");
                
                if(course.equals("Btech")){
                    if(branch.equals("CSE")){
                        setVisible(false);
                        new BtechCSEResult(rollno).setVisible(true);
                    }
                    if(branch.equals("EEE")){
                        setVisible(false);
                        new BtechEEEResult(rollno).setVisible(true);
                    }
                }
                if(course.equals("Mtech")){
                    if(branch.equals("CSE")){
                        setVisible(false);
                        new MtechCSEResult(rollno).setVisible(true);
                    }
                    if(branch.equals("EEE")){
                        setVisible(false);
                        new MtechEEEResult(rollno).setVisible(true);
                    }
                }
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Roll number is not registered!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

