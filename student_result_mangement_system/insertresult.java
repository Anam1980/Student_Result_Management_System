    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField1.getText();
        String course=(String)jComboBox1.getSelectedItem();
        String branch=(String)jComboBox2.getSelectedItem();
        
        if(rollno==null){
            JOptionPane.showMessageDialog(null, "RollNumber not entered!");
        }
        
        if(course.equals("Btech")){
        
            if(branch.equals("CSE")){
                setVisible(false);
                new BtechCSE(rollno, course, branch).setVisible(true);
            }
            else if(branch.equals("EEE")){
                setVisible(false);
                new BtechEEE(rollno, course, branch).setVisible(true);
            }
         else{
              JOptionPane.showMessageDialog(null, "Branch not entered!");
         }
         }
    else if(course.equals("Mtech")){
    
                if(branch.equals("CSE")){
                setVisible(false);
                new MtechCSE(rollno, course, branch).setVisible(true);
            }
                
                else if(branch.equals("EEE")){
                setVisible(false);
                new MtechEEE(rollno, course, branch).setVisible(true);
            }
                else{
                    JOptionPane.showMessageDialog(null, "Branch not entered!");
         }
       }
         else{
                    JOptionPane.showMessageDialog(null, "Select course!");
         }
      
    }//GEN-LAST:event_addActionPerformed

